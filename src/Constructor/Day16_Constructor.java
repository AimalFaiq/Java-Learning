package Constructor;

public class Day16_Constructor {
	Day16_Constructor(){
		System.out.println("I am the best");
		System.out.println("God is Great");
		// parameterized constructor
	}
		Day16_Constructor(String name){
			System.out.println("my name is"  + name);
		// parameterized double value constructor	
		}
		Day16_Constructor (String name , String Lastname){
			System.out.println("my name is :"  +name );
			System.out.println(" my lastname is :" + Lastname);
			
		}
		void hello () {
			System.out.println("i am the king");
		}
	}

